.MODEL SMALL
.DATA
STR1 DB "ENTER THE FIRST NUMBER $"
STR2 DB 13,10,"ENTER THE SECOND NUMBER $" 
STR3 DB 13,10,"SUM : $"
STR4 DB 13,10,"DIFFERENCE : $"
LOC DB 00H

DISPLAY MACRO R
MOV DL,R
ADD DL,30H
MOV AH,02H
INT 21H
ENDM

INP_CONV MACRO 
MOV AH,01H
INT 21H
SUB AL,30H
MUL CL
MOV BL,AL
MOV AH,01H
INT 21H 
SUB AL,30H
ADD AL,BL
ENDM

HEX_C MACRO R
MOV AL,R
MOV AH,00H
DIV CL
MOV BH,AH
MOV AH,00H
DIV CL
MOV CH,AH
MOV AH,00H
DIV CL
MOV DH,AH
DISPLAY AL
DISPLAY DH
DISPLAY CH
DISPLAY BH
ENDM

PRINT_S MACRO STR
LEA DX,STR
MOV AH,09H
INT 21H
ENDM

.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV CL,0AH
AND AX,0000H
PRINT_S STR1
INP_CONV
MOV CH,AL
MOV LOC,CH
PRINT_S STR2
INP_CONV
MOV BL,AL
ADD CH,BL
PRINT_S STR3
HEX_C CH
MOV CH,LOC
SUB CH,BL
PRINT_S STR4
HEX_C CH
MOV AH,4CH
INT 21H
MAIN ENDP
END






