.MODEL SMALL
.STACK 100H

.DATA
STR1 DB "ENTER THE FIRST NUMBER $"
STR2 DB 13,10,"ENTER THE SECOND NUMBER $" 
STR3 DB 13,10,"SUM : $"
NUM1 DW 1020H
NUM2 DW 0F00H
LOC DW 0000H

DISPLAY MACRO R
MOV DL,R
ADD DL,30H
MOV AH,02H
INT 21H
ENDM

INP_CONV MACRO 
MOV AH,01H
INT 21H
SUB AL,30H
MUL CL
MOV BL,AL
MOV AH,01H
INT 21H 
SUB AL,30H
ADD AL,BL
ENDM

HEX_C MACRO R
MOV AX,R
MOV CL,0A
DIV CL
MOV BH,AH
DIV CL
MOV CH,AH
MOV AH,00H
DIV CL
MOV DH,AH
DISPLAY AL
DISPLAY DH
DISPLAY CH
DISPLAY BH
ENDM

PRINT_S MACRO STR
LEA DX,STR
MOV AH,09H
INT 21H
ENDM


.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
XOR AX,AX
MOV AX,NUM1
MOV BX,NUM2
MOV DX,00
L1:DIV BX
CMP DX,00
JE L2
MOV AX,BX
MOV BX,DX
MOV DX,00
JMP L1
L2:MOV CX,BX
XOR AX,AX
XOR BX,BX
MOV AX,NUM1
MOV BX,NUM2
MUL BX
MOV BX,CX
DIV BX
MOV CX,AX
XOR BX,BX
MOV BX,CX
PRINT_S STR3

CALL DISPLAY
MOV AH,04H
INT 21H
MAIN ENDP
END
