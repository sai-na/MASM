ASSUME CS:CODE, DS:DATA
DATA SEGMENT

MSG1 DB "ENTER A STRING $"

STR  DB 100 DUP(?)

DATA ENDS

CODE SEGMENT

START:	MOV AX, DATA
	MOV DS, AX
	
	LEA DX, OFFSET MSG1
	MOV AH, 09H
	INT 21H
	
	MOV BX, 00H

READ:	MOV AH, 01H
	INT 21H
	CMP AL, 0DH
	JE STOP
	MOV [STR+BX], AL
	INC BX
	JMP READ
	
	
STOP:	DEC BX
	MOV DX, BX
	
	
AGAIN1: MOV CX, BX
	LEA SI, STR
AGAIN2:	MOV AL,[SI]
	CMP AL, 1[SI]
	JL SKIP
	
	MOV AH, 1[SI]
	MOV 1[SI], AL
	MOV [SI], AH
	
SKIP:	INC SI
	LOOP AGAIN2
	DEC DX
	JNZ AGAIN1
	
	
	LEA DX, OFFSET STR
	MOV AH, 09H
	INT 21H


	MOV AH, 4CH
	INT 21H

CODE ENDS
END START
